function setIds(productsArr, productType) {
    for(let i = 0; i < productsArr.length; i++) {
        productsArr[i].productId = productType+i.toString();
        productsArr[i].id = productType+i.toString();
    }

    return productsArr;
}

function setPackagesTags() {
    packages.forEach(packageData => {
        let tags = [...packageData.products];
        tags.push(packageData.category);
        tags = tags.map(tag => tag.toLowerCase())
        packageData.tags = tags;
    })
}
